main(List<string> arguments){
  num num1=45;
  num num2=5;
  //arithmetic operators
  print(num1+num2);
  print(num1-num2);
  print(num1*num2);
  print(num1/num2);
  print(num1%4);
  //equality and relational operators
  print(num1==num2);
  print(num1!=num2);
  print(num1>=num2);
  print(num1<=num2);
}
